class TableauTests(Question):
    def _grp(self, sim, *gens):
        """
        Assert the simulator's stabilizer group equals the one generated by gens.
        """
        exp = canonicalize([parse_signed(g) for g in gens], sim.num_qubits)

        self.assertEqual(sim.canon(), exp, msg="stabilizer group mismatch")

    def test_zero_1q(self):
        """
        |0> is stabilized by +Z.
        """
        self._grp(Simulator(1), "+Z")

    def test_zero_3q(self):
        """
        |000> is stabilized by +ZII, +IZI, +IIZ.
        """
        self._grp(Simulator(3), "+ZII", "+IZI", "+IIZ")

    def test_h_plus(self):
        """
        H|0> = |+>, stabilized by +X.
        """
        s = Simulator(1).H(0)
        self._grp(s, "+X")

    def test_s_maps_x_to_y(self):
        """
        S maps X -> Y.
        """
        s = Simulator(1).H(0).S(0)
        self._grp(s, "+Y")

    def test_z_makes_minus(self):
        """
        Z|+> = |->, stabilized by -X.
        """
        s = Simulator(1).H(0).Z(0)
        self._grp(s, "-X")

    def test_y_gate(self):
        """
        Y|0> = i|1>, stabilized by -Z.
        """
        s = Simulator(1).Y(0)
        self._grp(s, "-Z")

    def test_s_dag_inverts_s(self):
        """
        S_DAG . S = identity.
        """
        s = Simulator(1).H(0).S(0).S_DAG(0)
        self._grp(s, "+X")

    def test_bell(self):
        """
        Bell state stabilizers +XX, +ZZ.
        """
        s = Simulator(2).H(0).CX(0, 1)
        self._grp(s, "+XX", "+ZZ")

    def test_ghz(self):
        """
        GHZ_3 group generated by XXX, ZZI, IZZ.
        """
        s = Simulator(3).H(0).CX(0, 1).CX(0, 2)
        self._grp(s, "+XXX", "+ZZI", "+IZZ")

    def test_cz_on_plus(self):
        """
        CZ|++> stabilized by XZ, ZX.
        """
        s = Simulator(2).H(0).H(1).CZ(0, 1)
        self._grp(s, "+XZ", "+ZX")

    def test_swap(self):
        """
        SWAP exchanges |+>|0> -> |0>|+>.
        """
        s = Simulator(2).H(0).SWAP(0, 1)
        self._grp(s, "+ZI", "+IX")

    def test_cx_varargs_pairs(self):
        """
        CX accepts flattened (control, target) pairs.
        """
        s = Simulator(3).H(0).CX(0, 1, 1, 2)  # GHZ via a chain
        self._grp(s, "+XXX", "+ZZI", "+IZZ")

    def test_random_clifford_inverse(self):
        """
        A random Clifford followed by its inverse returns to |0...0>.
        """
        rng = random.Random(7)
        n = 6
        s = Simulator(n)
        ops = []
        for _ in range(120):
            k = rng.random()
            if k < 0.34:
                q = rng.randrange(n)
                s.H(q)
                ops.append(("H", q))
            elif k < 0.67:
                q = rng.randrange(n)
                s.S(q)
                ops.append(("S", q))
            else:
                a = rng.randrange(n)
                b = rng.randrange(n)
                while b == a:
                    b = rng.randrange(n)
                s.CX(a, b)
                ops.append(("CX", a, b))
        for op in reversed(ops):
            if op[0] == "H":
                s.H(op[1])
            elif op[0] == "S":
                s.S_DAG(op[1])
            else:
                s.CX(op[1], op[2])  # CX is its own inverse
        zero = ["+" + "I" * i + "Z" + "I" * (n - 1 - i) for i in range(n)]
        self._grp(s, *zero)


class StandardTests(Question):
    def test_bell_stabilizers(self):
        """
        The Bell state H(0).CX(0,1) has canonical stabilizers +XX, +ZZ.
        """
        sim = Simulator(2).H(0).CX(0, 1)

        self.assertEqual(sim.canon(), ["+XX", "+ZZ"], msg="Bell stabilizers")

    def test_bell_correlation(self):
        """
        Measuring both Bell qubits yields equal outcomes across many seeds.
        """
        for seed in range(200):
            sim = Simulator(2, seed=seed).H(0).CX(0, 1)
            m0 = sim.M(0)
            m1 = sim.M(1)

            self.assertEqual(m0, m1, msg=f"Bell outcomes must agree (seed {seed})")

    def test_ghz_stabilizers(self):
        """
        GHZ on 4 qubits via H + CX chain has the expected canonical stabilizers.
        """
        sim = Simulator(4).H(0).CX(0, 1, 1, 2, 2, 3)
        expected = ["+XXXX", "+ZIIZ", "+IZIZ", "+IIZZ"]

        self.assertEqual(sim.canon(), expected, msg="GHZ4 stabilizers")

    def test_ghz_correlation(self):
        """
        GHZ all-Z measurements collapse to either all-zero or all-one.
        """
        for seed in range(150):
            sim = Simulator(4, seed=seed).H(0).CX(0, 1, 1, 2, 2, 3)
            outs = sim.M(0, 1, 2, 3)

            self.assertEqual(
                len(set(outs)), 1, msg=f"GHZ outcomes must all agree (seed {seed})"
            )

    def test_teleport_plus(self):
        """
        Teleporting |+> gives <X> = +1 on the output qubit q2.
        """
        for seed in range(12):
            sim = _teleport(lambda s: s.H(0), seed)

            self.assertEqual(
                sim.peek("IIX"), 1, msg=f"|+> teleported <X> (seed {seed})"
            )

    def test_teleport_minus(self):
        """
        Teleporting |-> gives <X> = -1 on the output qubit q2.
        """
        for seed in range(12):
            sim = _teleport(lambda s: s.H(0).Z(0), seed)

            self.assertEqual(
                sim.peek("IIX"), -1, msg=f"|-> teleported <X> (seed {seed})"
            )

    def test_teleport_zero(self):
        """
        Teleporting |0> gives <Z> = +1 on the output qubit q2.
        """
        for seed in range(12):
            sim = _teleport(lambda s: s, seed)

            self.assertEqual(
                sim.peek("IIZ"), 1, msg=f"|0> teleported <Z> (seed {seed})"
            )

    def test_teleport_one(self):
        """
        Teleporting |1> gives <Z> = -1 on the output qubit q2.
        """
        for seed in range(12):
            sim = _teleport(lambda s: s.X(0), seed)

            self.assertEqual(
                sim.peek("IIZ"), -1, msg=f"|1> teleported <Z> (seed {seed})"
            )

    def test_reset_to_zero(self):
        """
        R resets a qubit to |0>, so <Z> = +1 afterward.
        """
        sim = Simulator(1).X(0)
        sim.R(0)

        self.assertEqual(sim.peek("Z"), 1, msg="R must reset to |0>")

    def test_measure_reset(self):
        """
        MR measures the prior value then leaves the qubit in |0>.
        """
        sim = Simulator(1).X(0)
        out = sim.MR(0)

        self.assertEqual(out, 1, msg="MR must report the pre-reset value")

        self.assertEqual(sim.peek("Z"), 1, msg="MR must reset to |0>")

    def test_measure_then_reuse(self):
        """
        A qubit measured and reset can be re-prepared and remeasured.
        """
        sim = Simulator(1, seed=0).H(0)
        sim.MR(0)
        sim.X(0)

        self.assertEqual(sim.M(0), 1, msg="reused qubit must measure 1 after X")

    def test_stabilizer_deterministic(self):
        """
        On a Bell state, measure of ZZ and XX is a deterministic +1.
        """
        sim = Simulator(2).H(0).CX(0, 1)
        zz, zz_random = sim.measure("ZZ")
        xx, xx_random = sim.measure("XX")

        self.assertEqual(zz, 1, msg="Bell ZZ must be +1")

        self.assertFalse(zz_random, msg="Bell ZZ must be deterministic")

        self.assertEqual(xx, 1, msg="Bell XX must be +1")

        self.assertFalse(xx_random, msg="Bell XX must be deterministic")

    def test_stabilizer_random(self):
        """
        On a product state |00>, measuring XX is a random coin flip.
        """
        sim = Simulator(2, seed=0)
        _value, random = sim.measure("XX")

        self.assertTrue(random, msg="XX on |00> must be random")

    def test_noisy_flip_rate(self):
        """
        X_ERROR(p) flips |0> with empirical frequency close to p.
        """
        c = Circuit(1)
        c.X_ERROR(0, 0.25)
        c.M(0)
        recs = c.sample(5000, seed=2)
        rate = recs[:, 0].mean()

        self.assertLess(abs(rate - 0.25), 0.03, msg=f"flip rate {rate:.3f} != 0.25")

    def test_depolarize_flip_rate(self):
        """
        DEPOLARIZE1(0.75) on |0> gives a measured-one frequency near 0.5.
        """
        c = Circuit(1)
        c.DEPOLARIZE1(0, 0.75)
        c.M(0)
        recs = c.sample(5000, seed=3)
        rate = recs[:, 0].mean()

        self.assertLess(abs(rate - 0.5), 0.04, msg=f"depolarized {rate:.3f} != 0.5")

    def test_coherent_estimate(self):
        """
        For H(0).RZ(0, theta), estimate of <X> tracks cos(theta).
        """
        for theta in (0.0, math.pi / 4, math.pi / 2, math.pi):
            c = Circuit(1)
            c.H(0)
            c.RZ(0, theta)
            got = c.estimate("X", shots=5000, seed=7)
            want = math.cos(theta)

            self.assertLess(
                abs(got - want),
                0.05,
                msg=f"<X> for RZ({theta:.2f}): got {got:.3f} want {want:.3f}",
            )

    def test_fidelity_identical(self):
        """
        Two identical Bell states have fidelity 1.0.
        """
        a = Simulator(2).H(0).CX(0, 1)
        b = Simulator(2).H(0).CX(0, 1)

        self.assertEqual(fidelity(a, b), 1.0, msg="identical states fidelity 1")

    def test_fidelity_orthogonal(self):
        """
        Orthogonal |Phi+> and |Phi-> have fidelity 0.0.
        """
        phi_plus = Simulator(2).H(0).CX(0, 1)
        phi_minus = Simulator(2).X(0).H(0).CX(0, 1)

        self.assertEqual(
            fidelity(phi_plus, phi_minus), 0.0, msg="orthogonal fidelity 0"
        )

    def test_fidelity_half(self):
        """
        |0> and |+> overlap with fidelity 0.5.
        """
        zero = Simulator(1)
        plus = Simulator(1).H(0)

        self.assertEqual(fidelity(zero, plus), 0.5, msg="|0> vs |+> fidelity 0.5")


class MeasureTests(Question):
    def test_zero_deterministic(self):
        """
        Measuring |000> gives 0 on every qubit.
        """
        self.assertEqual(
            Simulator(3).M(0, 1, 2), [0, 0, 0], msg="|000> must measure all zero"
        )

    def test_x_gives_one(self):
        """
        X|0> measures 1.
        """
        self.assertEqual(Simulator(1).X(0).M(0), 1, msg="X|0> must measure 1")

    def test_repeatable(self):
        """
        Measuring the same qubit twice agrees.
        """
        s = Simulator(1).H(0)

        self.assertEqual(s.M(0), s.M(0), msg="repeated measurement must agree")

    def test_bell_correlation(self):
        """
        Bell-pair outcomes are perfectly correlated.
        """
        for seed in range(200):
            s = Simulator(2, seed=seed).H(0).CX(0, 1)

            self.assertEqual(s.M(0), s.M(1), msg="Bell outcomes must correlate")

    def test_plus_statistics(self):
        """

        |+> measures 0/1 about 50:50.
        """
        outs = [Simulator(1, seed=i).H(0).M(0) for i in range(4000)]

        self.samplesClose(
            outs,
            {
                0: 0.5,
                1: 0.5,
            },
            atol=0.04,
            msg="|+> should measure 0/1 evenly",
        )

    def test_ghz_statistics(self):
        """
        GHZ_3 collapses only to 000 or 111, about 50:50.
        """
        c = Counter()
        n = 3000
        for i in range(n):
            s = Simulator(3, seed=i).H(0).CX(0, 1).CX(0, 2)
            c[tuple(s.M(0, 1, 2))] += 1

        self.assertEqual(
            set(c) - {(0, 0, 0), (1, 1, 1)},
            set(),
            msg="GHZ must collapse only to 000 or 111",
        )

        self.assertLess(
            abs(c[(0, 0, 0)] / n - 0.5), 0.05, msg="000 share should be near 50%"
        )

    def test_record(self):
        """
        Measurements append to the record in order.
        """
        s = Simulator(2).X(0)
        s.M(0)
        s.M(1)

        self.assertEqual(s.record, [1, 0], msg="record must reflect measurement order")


