def _teleport(prepare, seed):
    """
    Teleport a state prepared on q0 through a Bell pair (q1, q2) onto q2.
    """
    sim = Simulator(3, seed=seed)
    prepare(sim)
    sim.H(1).CX(1, 2)
    sim.CX(0, 1).H(0)
    if sim.M(1) == 1:
        sim.X(2)
    if sim.M(0) == 1:
        sim.Z(2)

    return sim


